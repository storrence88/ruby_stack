array = ["Steven", "Meredith", "Mike", "Stephen", "Derek", "Casey"]

#################################################

p array.any? { |word| word.length >= 3} # Returns "true"

#################################################

p array.each { |word| print word, "--"} # Steven--Meredith--Mike--Stephen--Derek--Casey--

